#include <iostream>

int main() {
    int num1, num2, sum;

    // Prompt user for input
    std::cout << "Enter two integers: ";

    // Read user input
    std::cin >> num1 >> num2;

    // Calculate sum
    sum = num1 + num2;

    // Display result
    std::cout << num1 << " + " << num2 << " = " << sum << std::endl;

    return 0;
}
